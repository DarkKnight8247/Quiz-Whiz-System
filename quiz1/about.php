                <p>The system is comprised of 6 pages, these are, the start up menu, selection menu, about page (this page), and the remaining 3 are the categories.</p>
                <p>One of the core features of the system is the use of foreach loops. These loops are crucial part to displaying questions and choices dynamically. By using loops to handle the question sets, the system ensures that questions are presented in a flexible and scalable manner, allowing for easy updates or additions to the question database in the future.</p>
