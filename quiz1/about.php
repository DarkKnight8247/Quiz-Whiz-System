                <p>The Quiz Whiz system currently offers a selection of three categories for users to choose from:</p>
                <p>
                - Science
                - English
                - Math
                </p>
                <p>Upon selecting a category, users are presented with a series of multiple-choice questions, which test their understanding and knowledge in the chosen field. We also planned to add a timer and difficulty level but since we are only required to make a mini-system we have to change the plan.</p>
